//单目操作符：只有一个操作数通俗一点就是只有一边有数值a-- s++ 这些就是单目操作符
void main()
{
    int a = 10;
    printf("%d\n",!a);//！就是把真变假，把假变真，0为假，真为非0的数,只要不是0就是真

    a = -6;//负号

    int* b = 0;
    b= &a; //&取地址操作符
    *b=6;//*解引用操作符//意思就是通过b里面的地址找到存储的对象，=6就是把6赋值到这个变量b存储的地址里面的数值或者说的对象
    printf("%p\n",b);

    int c=sizeof(a);//就是用来计算变量，数值，指针，类型这些所占的的空间大小 大小的单位是4字节
    printf("%d\n", c);
}
