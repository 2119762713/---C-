//再次实现strlen函数
int my_strlen(const char* str)
{
	assert(str !=NULL);//保证指针的有效性
	int i = 0;
	while(*str!='\0')
	{ 
		i++;
		str++;
	}
	return i;
}

void main()
{
	char arr[] = "ABCDEF";
	printf("%d\n", my_strlen(arr));
}
